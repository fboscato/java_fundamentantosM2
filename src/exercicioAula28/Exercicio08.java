package exercicioAula28;

public class Exercicio08 {

	public static void main(String[] args) {
		int num1 = 11;
		float num2 = 22.22f;
		double num3 = 33.33;
		
		// Aplicação de wrappers e auto boxing"
		Integer w1 = num1;
		Float w2 = num2;
		Double w3 = num3;
		
		System.out.println("Integer para int: " + w1);
		System.out.println("Float para float: " + w2);
		System.out.println("Double para double: " + w3);

	}

}
